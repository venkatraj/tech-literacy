function tech_literacy_boolean($value) {
	if(is_bool($value)) {
		return $value;
	} else {
		return false;
	}
}

function tech_literacy_breadcrumb_char_choices($value='') {
	$choices = array('1','2','3');

	if( in_array($value, $choices)) {
		return $value;
	} else {
		return '1';
	}
}

